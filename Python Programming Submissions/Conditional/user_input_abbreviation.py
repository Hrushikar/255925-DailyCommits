string = input('Enter the abbreviation: ')
if string == 'lol' or string == 'LOL' or string == 'LoL':
    print('Laughing out loud')
elif string == 'rofl' or string == 'ROFL' or string == 'RoFL':
    print('Rolling on the floor laughing')
elif string == 'lmk' or string == 'LMK':
    print('Let me know')
elif string == 'smh' or string == 'SMH':
    print('Shaking my head')
